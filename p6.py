import numpy

a = numpy.array([[1, 2], [3, 4]])

b = numpy.array([[5, 6], [7, 8]])

print("Addition of two matrix: ", numpy.add(a,b))

print("Subtraction of two matrix : ", numpy.subtract(a,b))

print("Division of two matrix : ", numpy.divide(a,b))

print("Multiplication of two matrix: ", numpy.multiply(a,b))

print("Dot product of two matrix : ", numpy.dot(a,b))